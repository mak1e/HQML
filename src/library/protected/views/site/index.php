<h1>What is a healthcare measure?</h1>
<p>Healthcare measures are developed to help us understand and assess changes in the delivery of healthcare services and the impacts achieved, for example, on patient health outcomes.  They can be applied at practice, organisational and population levels to support quality improvement.</p>
<br />
<h1>What is Healthcare Quality Measures NZ?</h1>
<p><i>Driving consistent ways of measuring healthcare in New Zealand.</i></p>
<br />
<p>Healthcare Quality Measures NZ is a searchable resource of all health measure definitions, derivations and their constructions. Healthcare Quality Measures NZ is designed to be for the sector by the sector and provides a platform for consistent ways of measuring Healthcare as a key resource in measuring quality.  It is an active resource for anyone to use measures from and contribute measures to in a structured way.</p>
<br />
<h1>What is the purpose of Healthcare Quality Measures NZ?</h1>
<p>The overall goal of Healthcare Quality Measures NZ is to improve patient health outcomes in NZ, by providing an easily accessible, reliable resource of measures relating to health outcomes and/or healthcare delivery to support the healthcare sector to get a clearer focus on information and how it can be applied at practice and aggregate levels to influence clinical quality improvement.</p>
<p>Healthcare Quality Mesaures NZ will:</p>
<ul class="indented">
    <li>support improvement in patient health outcomes by improving quality of care through the provision of information; </li>
    <li>provide the front-line with tools needed to provide nationally consistent data;</li>
    <li>support learning and peer review in practice settings;</li>
    <li>support the sharing of information in order to improve awareness of what measures exist and how they may be applied;</li>
    <li>reduce fragmentation/duplication of effort in applying and developing new measures for use in the health sector;</li>
    <li>standardise a format for definition of measures (including provision of clear directions as to why and how each measure should be applied) that facilitates ease of comparison;</li>
    <li>introduce a non-partisan, collaborative process, bringing stakeholders together in order to:</li>
        <ul class="indented">
            <li>establish a 'critical mass' of interest, expertise and experience; and</li>
            <li>provide a place and agreed processes within which to position and take forward ideas generated across this sector</li>
        </ul>
</ul>
<br />
<h1>Who should use Healthcare Quality Measures NZ and why?</h1>
<p>Healthcare Quality Measures NZ will be valuable to a wide range of people, from community and primary health care clinicians (e.g. GPs; practice nurses; mental health service providers), to health funders, academic institutions and members of the public. The range of potential uses is diverse; some key examples include:</p>
<ul class="indented">
    <li>To assist in the process of interpreting data made available in publicly released health reports.</li>
    <li>To support clinicians in the use of healthcare measures to facilitate a peer review and professional development process. </li>
    <li>To support development of a coherent framework for monitoring implementation of new policy initiatives in various health care settings (e.g. implementation of business cases for development of Integrated Family Health Care Centres).</li>
    <li>TO provide a platform for definitions that facilitates dialogue between local and international healthcare professionals.</li>
    <li>To provide a trusted and up-to-date source of common definition of measurements, for example for use by system developers to indentify opportunities for enhancement of measurement reporting tools.</li>
    <li>To provide a reliable source of measures to support research or analysis of a particular theme.</li>
</ul>
