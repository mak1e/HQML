<h1>What is a measure?</h1>
<p>Measures are generated by organisations in order to understand and assess how health services are operating and are continually reviewed and changed over time to support the various policy and service developments that occur within health.</p>
<br />
<h1>What is the Health Measures Library?</h1>
<p>Presently a vast number of healthcare measures exist. These measures have been developed by a variety of organisations for many reasons. A lack of knowledge and coordination across the health sector is resulting in fragmentation when developing measures and duplication of effort.<br />
  Until the development of the HQMNZ, there was no single repository which allowed users to obtain a complete set of NZ health measures.</p>
<br />
<h1>Why do we need a Health Measures Library?</h1>
<p>Presently a vast number of health care measures exist . These measures have been developed by a variety of organisations for many reasons. A lack of knowledge and coordination across the health sector is resulting in fragmentation when developing measures and duplication of effort.</p>
<p>Until the development of this Health Care Quality Measures Library, there was no single repository which allowed users to obtain a complete set of NZ health measures.</p>
<br />
<h1>What are the benefits of a Health Measures Library?</h1>
<p>HQMNZ will be valuable to a wide range of people, but of particular relevance to those looking to establish measures for a service, theme or topic, and needing to research what might already be available or re-purposed.</p>
<br />
<h1>How to use the Health Measures Library</h1>
<p>To start searching for a health measure(s) simply click on the button at the bottom of this page.<br />
  Begin typing key words on which your search is based (in the designated search area) and you will be presented with a list of relevant words or terms as specific key words are located. Make your selection by clicking on your chosen search term. Alternatively you may type words which although not flagged as key words, will result in a match if the text exists within HQMNZ.<br />
  This program will find all measures that match that search terms you have used and these will be presented to you as a summarised list. You will then be able to select one or multiple matches, at which point all information pertaining to your selection will be displayed in table format. To find out what each of the table headings mean, simply hover over the field â€“ the definition of that heading will then be displayed.</p>
</p>
